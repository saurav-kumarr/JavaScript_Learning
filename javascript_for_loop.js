// For and While loops

// For Loops
for(let i = 0;i < 10;i++){
    console.log(i);
}


for(let i = 10;i >= 0;i--){
    console.log(i);
}

let numbers = [1,2,3,4,5,6,7,8];
for(let i=0;i<numbers.length;i++){
    console.log(numbers[i]);
}

for(let i = 0;i < 5;i++){
    for(let j = 0;j<2;j++){
    console.log(j);
  }
}

// While Loop
// Executes the statements as long as condition
// evalutes to true

let i=0;
while (i<5) {
    console.log(i);
    i++;
}